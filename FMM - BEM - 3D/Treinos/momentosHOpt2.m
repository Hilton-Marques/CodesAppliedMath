    obj = SValues(i);
    objAn2 = SValues(i-2);
    objAn1 = SValues(i-1);
    mEnd = 1+(obj.m-1)/2;
    for j = 1:mEnd-2
        MValue = ir2*(-((i-2) + (j-1))*((i-2) - (j-1))*objAn2.MValuesPos(j) + ...
            (2*(i-2) + 1)*y(3)*objAn1.MValuesPos(j) );
