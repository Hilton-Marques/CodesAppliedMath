    j = j + 1;
    MValue = ir2*(2*(i-2) + 1)*y(3)*objAn1.MValuesPos(j);
    obj.addMValues(MValue,j);
    obj.merge();
end
out = [SValues(:).values]';
if ~isempty(m)
    level = SValues(end).values';
    out = level(m);
end

end

